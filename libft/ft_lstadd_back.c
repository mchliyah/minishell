#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	if (!lst || !new)
		return ;
	if (*lst == 0)
		*lst = new;
	else
	{
		new->prev = ft_lstlast(*lst);
		(ft_lstlast(*lst))->next = new;
	}
}
